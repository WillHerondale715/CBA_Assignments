public class assignment {
    public static void main(String[] args) {
        int x;

        x = 20;
        x += 5;
        System.out.println("After x += 5: " + x);

        x = 10;
        x -= 5;
        System.out.println("After x -= 5: " + x);

        x = 15;
        x *= 5;
        System.out.println("After x *= 5: " + x);

        x = 10;
        x /= 5;
        System.out.println("After x /= 5: " + x);

        x = 25;
        x %= 5;
        System.out.println("After x %= 5: " + x);
    }
}
